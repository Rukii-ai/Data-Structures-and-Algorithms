"""Code example of a program with O(n) time complexity."""

def print_items(n):
    """Define a function that prints numbers from 0 to n-1."""
    for i in range(n):
        print(i)

print_items(10)
"""
This is an example of a program with O(n) time complexity.

We passed it the number n and it ran n times, so whatever n is that is the
number of operations

O(n) is always going to be a straight line because it is proportional to n
"""